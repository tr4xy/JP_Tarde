package Aula04;

public class UsandoSwitchCase {

	public static void main(String[] args) {
		String op = Calça;
		
		switch (op) {
		case "Camiseta":
			System.out.println("Gaveta 01");
			break;
		case "Calça":
			System.out.println("Gaveta 02");
			break;
		case "Sapato":
			System.out.println("Gaveta 03");
			break;
			
		default:
			break;
		}
		

	}

}
